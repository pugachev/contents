<?php

$array = [
    [100, 200, 300, 400, 500],
    [600, 700, 800, 900, 1000],
    [1100, 1200, 1300, 1400, 1500],
];

// 3行5列の配列で左下から右上に走査
for ($i = count($array) - 1; $i >= 0; $i--) {
    for ($j = 0; $j < count($array[$i]); $j++) {
        // 最初の行に到達するまで列を増やす
        if ($j === count($array) - 1 - $i) {
            echo $array[$i][$j] . ' ';
        }
    }
}