<?php

$array = [
    [100, 200, 300, 400, 500],
    [600, 700, 800, 900, 1000],
    [1100, 1200, 1300, 1400, 1500],
];

// 配列の行数を取得
$rows = count($array);
// 配列の列数を取得
$cols = count($array[0]);

// 左上から右下まで走査
for ($i = 0; $i < $rows; $i++) {
    for ($j = 0; $j < $cols; $j++) {
        // 対角線上の要素のみを出力
        if ($i == $j) {
            echo $array[$i][$j] . ' ';
        }
    }
}
